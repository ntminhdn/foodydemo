public class DataAnGiObject {
    public String restName;
    public String address;
    public String photo;
    public String foodName;
    public String avatar;
    public String username;
    public String time;

    public DataAnGiObject(String restName, String address, String photo, String foodName, String avatar, String username, String time) {
        this.restName = restName;
        this.address = address;
        this.photo = photo;
        this.foodName = foodName;
        this.avatar = avatar;
        this.username = username;
        this.time = time;
    }
}
